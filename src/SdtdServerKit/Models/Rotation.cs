namespace SdtdServerKit.Models
{
    /// <summary>
    /// Represents the rotation of an object.
    /// </summary>
    public enum Rotation
    {
        /// <summary>
        /// No rotation.
        /// </summary>
        Rot0 = 0,

        /// <summary>
        /// 90 degrees rotation.
        /// </summary>
        Rot90 = 1,

        /// <summary>
        /// 180 degrees rotation.
        /// </summary>
        Rot180 = 2,

        /// <summary>
        /// 270 degrees rotation.
        /// </summary>
        Rot270 = 3
    }
}
